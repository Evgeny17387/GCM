package Objects;

public class Map {
	
	public String mName;
	public String mCity;
	public int mVersion;
	public String mDescription;

	public Map(String aName, String aCity, int aVersion, String aDescription) {
		this.mName = aName;
		this.mCity = aCity;
		this.mVersion = aVersion;
		this.mDescription = aDescription;
	}
	
}
