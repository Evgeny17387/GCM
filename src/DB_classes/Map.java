package DB_classes;

public class Map {

	public String mName;
	public int mVersion;
	public String mCity;
	public String mDescription;
	
	public Map(String aName, int aVersion, String aCity, String aDescription){
    	this.mName = aName;
    	this.mVersion = aVersion;
    	this.mCity = aCity;
    	this.mDescription = aDescription;
	}

}
