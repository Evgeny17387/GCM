package DB_classes;

public class AccountWorker {

	public int mId;
	public String mName;
	public String mPassword;
	public String mType;

	public AccountWorker(int aId, String aName, String aPassword, String aType) {
		this.mId = aId;
		this.mName = aName;
		this.mPassword = aPassword;
		this.mType = aType;
	}

}
